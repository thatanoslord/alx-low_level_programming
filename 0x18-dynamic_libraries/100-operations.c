#include <stdio.h>
/**
 *  * add - add 2 integers
 *   * @a: integer 1
 *    * @b: integer 2
 *     * Return: result
 *      */

int add(int a, int b)
{
		return (a + b);
}

/**
 *  * sub - substitute 2 integers
 *   * @a: integer 1
 *    * @b: integer 2
 *     * Return: result
 *      */

int sub(int a, int b)
{
		return (a - b);
}

/**
 *  * mul - multiply 2 integers
 *   * @a: integer 1
 *    * @b: integer 2
 *     * Return: result
 *      */

int mul(int a, int b)
{
		return (a * b);
}

/**
 *  * div - divide 2 integers
 *   * @a: integer 1
 *    * @b: integer 2
 *     * Return: result
 *      */

int div(int a, int b)
{
		return (a / b);
}

/**
 *  * mod - modulus 2 integers
 *   * @a: integer 1
 *    * @b: integer 2
 *     * Return: result
 *      */

int mod(int a, int b)
{
		return (a % b);
}
